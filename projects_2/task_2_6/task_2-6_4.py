option = input("Перед вами три дороги: направо, налево, прямо. Выберите, куда отправиться (направо/налево/прямо): ").strip()
if option == "направо" or "Направо": 
    print("Направо поедешь - богату быти")
elif option == "налево" or "Налево":
    print("Налево поедешь - женату быти")
elif option == "прямо" or "Прямо":
    print("Прямо поедешь - жизни не бывати")