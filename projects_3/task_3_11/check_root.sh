#!/bin/bash

# Функция проверки прав root
check_root() {
    # $EUID - эффективный UID текущего пользователя
    if [[ $EUID -ne 0 ]]; then
        echo "Ошибка: этот скрипт запущен не от имени суперпользователя." >&2
        exit 1
    fi
}

# --- Основная часть скрипта ---
check_root

echo "Скрипт запущен от root. Продолжаем работу..."
